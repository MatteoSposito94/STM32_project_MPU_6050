#define MPU6050_ADDR 			0XD0
#define SMPLRT_DIV_REG 			0X19
#define GYRO_CONFIG_REG 		0X1B
#define ACCEL_CONFIG_REG 		0X1C
#define ACCEL_XOUT_H_REG 		0X3B
#define TEMP_OUT_H_REG 			0X41
#define GYRO_XOUT_H_REG 		0X43
#define PWR_MGMT_1_REG 			0X6B
#define WHO_AM_I_REG 			0X75

int16_t Accel_X_RAW = 0;   // accelerazione
int16_t Accel_Y_RAW = 0;
int16_t Accel_Z_RAW = 0;

int16_t Gyro_X_RAW = 0;   // giroscopio
int16_t Gyro_Y_RAW = 0;
int16_t Gyro_Z_RAW = 0;

uint8_t Rec_Data1[6];  // dati acquisiti salvati in un registro
uint8_t Rec_Data2[6];

double Ax, Ay, Az, Gx, Gy, Gz;

// funzione per inizializzare tutte le periferiche dell'accelerometro/giroscopio

void MPU6050_Init (void)
{
	uint8_t check;
	check = 0;

	uint8_t Data;


	// guardo se il sensore è presente e può lavorare
	// leggo all'indirizzo 0x75 (WHO I AM)
	// se 0x68 o 104 allora vuol dire che è presente e inizia a lavorare bene
	HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, WHO_AM_I_REG, 1 ,&check, 1, HAL_MAX_DELAY);

	if (check == 104) // verifica presenza MPU
	{
		// registro di alimentazione PWR_MGMT_1 - tutti i bit a 0, riattivo lo slave gli dico tieniti pronto
		Data = 0;
		HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, PWR_MGMT_1_REG, 1, &Data, 1, 1000);
		// imposto frequenza, sample rate, con valore 7 in registro 0x19
		Data = 0x07;
		HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, SMPLRT_DIV_REG, 1, &Data, 1, 1000);
		// configuro accelerometro XA_ST=0; YA_ST=0; ZA_ST=0; 2g
		Data = 0x00;
		HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, ACCEL_CONFIG_REG, 1, &Data, 1, 1000);
		// configuro giroscopio XG_ST=0; YG_ST=0; ZG_ST=0; 250 deg/s
		Data = 0x00;
		HAL_I2C_Mem_Write(&hi2c1, MPU6050_ADDR, GYRO_CONFIG_REG, 1, &Data, 1, 1000);
	}

}


void MPU6050_Read_Accel(void)
{

	// leggo 6 byte dal reg ACCEL_XOUT_H

	HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, ACCEL_XOUT_H_REG, 1, Rec_Data1, 6, 1000);

	Accel_X_RAW = (int16_t)(Rec_Data1 [0] << 8 | Rec_Data1 [1]);
	Accel_Y_RAW = (int16_t)(Rec_Data1 [2] << 8 | Rec_Data1 [3]);
	Accel_Z_RAW = (int16_t)(Rec_Data1 [4] << 8 | Rec_Data1 [5]);

	// ora faccio la conversione in "g" e dobbiamo dividere per 16384.0 (FS_SEL=0 per forza da datasheet)

	Ax = Accel_X_RAW/16384.0;
	Ay = Accel_Y_RAW/16384.0;
	Az = Accel_Z_RAW/16384.0;
}

void MPU6050_Read_Gyro(void)
{

	// leggo 6 byte dal reg GYRO_XOUT_H

	HAL_I2C_Mem_Read(&hi2c1, MPU6050_ADDR, GYRO_XOUT_H_REG, 1, Rec_Data2, 6, 1000);

	Gyro_X_RAW = (int16_t)(Rec_Data2 [0] << 8 | Rec_Data2 [1]);
	Gyro_Y_RAW = (int16_t)(Rec_Data2 [2] << 8 | Rec_Data2 [3]);
	Gyro_Z_RAW = (int16_t)(Rec_Data2 [4] << 8 | Rec_Data2 [5]);

	// ora faccio la conversione in "deg/s" e dobbiamo dividere per 131.0 (FS_SEL=0 per forza da datasheet)

	Gx = Gyro_X_RAW/131.0;
	Gy = Gyro_Y_RAW/131.0;
	Gz = Gyro_Z_RAW/131.0;
}

